package com.ruoyi.common.isky;

public class Block {
	int xstart;
	int ystart;
	int xend;
	int yend;

	public int getXstart() {
		return xstart;
	}

	public void setXstart(int xstart) {
		this.xstart = xstart;
	}

	public int getYstart() {
		return ystart;
	}

	public void setYstart(int ystart) {
		this.ystart = ystart;
	}

	public int getXend() {
		return xend;
	}

	public void setXend(int xend) {
		this.xend = xend;
	}

	public int getYend() {
		return yend;
	}

	public void setYend(int yend) {
		this.yend = yend;
	}
}
